package week2;

public class DeclaringEx1 {
	static float interestRate = 5.75f;
	static int age = 38;
	static char initial = 'A';
	public static void main(String[] args) {
		System.out.println("The interest rate is "+interestRate);
		System.out.println("The initial character is "+initial);
		System.out.println("The age is "+age);

	}

}
